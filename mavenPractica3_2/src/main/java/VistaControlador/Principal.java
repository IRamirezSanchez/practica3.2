    private void botonGuardarTextAreaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarTextAreaActionPerformed
        JFileChooser seleccionarArchivo = new JFileChooser();
        int solucion = seleccionarArchivo.showSaveDialog(null);

        if (JFileChooser.APPROVE_OPTION == solucion) {
            try {
                Ficheros.guardarFichero(areaTextoPrincipal.getText(), seleccionarArchivo);
            } catch (IOException ex) {
                barraEstadoTiempo("No se ha podido Guardar el Fichero");
            }
            barraEstadoTiempo("Texto Guardado con EXITO!");
        }

    }//GEN-LAST:event_botonGuardarTextAreaActionPerformed

    private void botonLeerTextAreaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonLeerTextAreaActionPerformed
        JFileChooser seleccionarArchivo = new JFileChooser();
        int solucion = seleccionarArchivo.showOpenDialog(null);

        if (JFileChooser.APPROVE_OPTION == solucion) {
            try {
                areaTextoPrincipal.setText(Ficheros.leerFichero(seleccionarArchivo));
            } catch (IOException ex) {
                barraEstadoTiempo("No se ha podido Cargar el Fichero");
            }
            barraEstadoTiempo("Texto Cargado con EXITO!");
        }

    }//GEN-LAST:event_botonLeerTextAreaActionPerformed

    private void botonEdicionColorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEdicionColorActionPerformed
        JColorChooser elegir = new JColorChooser();
        Color colorSeleccionado = elegir.showDialog(null, "Selecciona un Color", panelLateralPrincipal.getBackground());       
        if (colorSeleccionado != null) {                    
            Color colorSel2 = colorSeleccionado.darker(); //Cojo el color de arrriba y le doy oscuridad y lo pongo en una variable Color para utilizarla.
            cambiarColorPrincipal(colorSel2, colorSeleccionado); 
            barraEstadoTiempo("Se ha cambiado de COLOR la UI");
        }
    }//GEN-LAST:event_botonEdicionColorActionPerformed
    
    
